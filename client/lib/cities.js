// برچسب نمایش انتخاب: یکی → نام شهر | چندتا → «۲ شهر»
export function cityLabel(cities) {
  if (!cities || cities.length === 0) return 'همه شهرها';
  if (cities.length === 1) return cities[0];
  return `${cities.length.toLocaleString('fa-IR')} شهر`;
}

// پارس پارامتر URL (با کاما جدا شده)
export function parseCities(param) {
  if (!param) return [];
  return String(param).split(',').map((s) => s.trim()).filter(Boolean);
}
